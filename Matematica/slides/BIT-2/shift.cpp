#include <bits/stdc++.h>

using namespace std;

int main()
{
    int a = 12345, b = -98;

    cout << (a << 2) << endl;   // 49380
    cout << (a >> 3) << endl;   // 1543

    cout << (b << 2) << endl;   // -392
    cout << (b >> 3) << endl;   // -13

    cout << b / 8 << endl;      // -12

    return 0;
}
